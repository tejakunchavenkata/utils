sub printnewpfs { print "$_ -> $cnt{$_}" foreach (sort {$a <=> $b} keys %cnt) }

sub div () {
  my ($x, $y, $z) = @_;
  
  my $product = $x * $y * $z;
  if (not exists $cnt{$product}) {
    my %allpfs;
    $allpfs{$_}++ foreach ( @{$pfs{$x}}, @{$pfs{$y}}, @{$pfs{$z}} );
    $cnt{$product} = 1;
    $cnt{$product} *= $_ + 1 foreach values %allpfs;
  }

  return $cnt{$product};
}

my ($x, $y, $z, $buf);
chomp ($buf = <STDIN>);
($x, $y, $z) = split /\s+/, $buf;

my $res = 0;
foreach my $i (1 .. $x) {
  foreach my $j (1 .. $y) {
    foreach my $k (1 .. $z) {
      $res += &div ($i, $j, $k);
    }
  }
}
print $res;
